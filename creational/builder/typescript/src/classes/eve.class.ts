export class Eve {
    hairLong: string;
    eyesColor: string;
    hasGlasses: boolean;
    outfit: string;
    weapon: string;
    level: number;

    constructor(hairLong: string, eyesColor: string, hasGlasses: boolean, outfit: string, weapon: string, level: number) {
        this.hairLong = hairLong;
        this.eyesColor = eyesColor;
        this.hasGlasses = hasGlasses;
        this.outfit = outfit;
        this.weapon = weapon;
        this.level = level;
    }
}